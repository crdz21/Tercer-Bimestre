/* JavaScript trabaja principalmente con dos tipos de variables, que pueden comenzar con con VAR y o con LET*/

// <- Usar doble slash(/) solo funciona para poner comentarios en UNA SOLA LÍNEA, para comentar en más de una se usan otras tipologías.



/* Con var, las variables se vuelven GLOBALES, usables para todo el archivo con el mismo valor. */

var nombre = "Mateo";



// Al contrario que las varuables VAR, con LET se definen variables LOCALES, solo usables en un bloque, no son repetibles.

let Código;


document.write(nombre);